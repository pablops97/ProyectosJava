    private void mostrar() throws SQLException {
        Manga manga = new Manga();
        
        ConsultaDetalle.iniciarResultSet(user);
        
        manga = ConsultaDetalle.inicial(user);
        
        txtIdManga.setText(""+manga.getId());
        txtCodigoUsuario.setText(""+manga.getCodigoUsuario());
        txtNombreManga.setText(""+manga.getNombre());
        txtPrecio.setText(""+manga.getPrecio());
        txtDescripcion.setText(""+manga.getDescripcion());
    }
    
    private void mostrarSiguiente() throws SQLException {
        Manga manga = new Manga();
        
        manga = ConsultaDetalle.siguiente(user);
        
        txtIdManga.setText(""+manga.getId());
        txtCodigoUsuario.setText(""+manga.getCodigoUsuario());
        txtNombreManga.setText(""+manga.getNombre());
        txtPrecio.setText(""+manga.getPrecio());
        txtDescripcion.setText(""+manga.getDescripcion());
        
        if(ConsultaDetalle.isLast())
        {
            btnAvanzar.setEnabled(false);
        }
        else
        {
            btnAvanzar.setEnabled(true);
        }
    }
    
    private void mostrarAnterior() throws SQLException {
        Manga manga = new Manga();
        
        manga = ConsultaDetalle.atras(user);
        
        txtIdManga.setText(""+manga.getId());
        txtCodigoUsuario.setText(""+manga.getCodigoUsuario());
        txtNombreManga.setText(""+manga.getNombre());
        txtPrecio.setText(""+manga.getPrecio());
        txtDescripcion.setText(""+manga.getDescripcion());
        
        if(ConsultaDetalle.isFirst())
        {
            btnRetroceder.setEnabled(false);
        }
        else
        {
            btnRetroceder.setEnabled(true);
        }
    }
