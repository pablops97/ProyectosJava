    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed

                // Mostrar el JOptionPane con un campo de entrada de texto
        String aux;
        try {
            JTextField textField = new JTextField();
            Object[] message = {"", textField};
            int option = JOptionPane.showConfirmDialog(null, message, "Ingresar Texto", JOptionPane.OK_CANCEL_OPTION);

            // Obtener el texto ingresado si se hizo clic en "OK"
            if (option == JOptionPane.OK_OPTION) {
                aux = textField.getText();
                Manga m = new Manga(Integer.parseInt(txtIdManga.getText()), Integer.parseInt(txtCodigoUsuario.getText()), txtNombreManga.getText(), Double.parseDouble(txtPrecio.getText()), aux);
                ConsultaDetalle.ActualizarManga(m);
                mostrar();
            }

            

            
        } catch (SQLException ex) {
            Logger.getLogger(VistaDetalle.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed


